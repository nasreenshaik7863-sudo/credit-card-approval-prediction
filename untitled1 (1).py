import pandas as pd
print("My fresh notebook is ready!")

# Pulling the dataset directly from an online copy so it always loads perfectly
url = "https://raw.githubusercontent.com/nsethi31/Kaggle-Data-Credit-Card-Fraud-Detection/master/creditcard.csv"
df = pd.read_csv(url)

# Display the row count and the first few rows
print(f"Success! Loaded {len(df)} transactions.")
df.head()

# Count how many genuine (0) and fraudulent (1) transactions are in the data
print("Transaction Breakdown:")
print(df['Class'].value_counts())

# Calculate the exact percentage of fraud cases
fraud_percentage = (df['Class'].value_counts()[1] / len(df)) * 100
print(f"\nPercentage of Fraudulent Transactions: {fraud_percentage:.4f}%")

# Drop the target answer column to isolate our features (X)
X = df.drop(columns=['Class'])

# Isolate the answer column as our target (y)
y = df['Class']

print("Features (X) shape:", X.shape)
print("Target (y) shape:", y.shape)

from sklearn.model_selection import train_test_split

# Split the data: 80% for training, 20% for testing
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.20, random_state=42, stratify=y)

print(f"Training features size: {X_train.shape[0]} rows")
print(f"Testing features size: {X_test.shape[0]} rows")

from sklearn.ensemble import RandomForestClassifier

# Initialize the Random Forest model
# We set n_estimators=100 (100 decision trees) and max_depth=10 to keep it lightweight
model = RandomForestClassifier(n_estimators=100, max_depth=10, random_state=42, n_jobs=-1)

print("Training the AI model now... (this may take a few seconds)...")

# Teach the model using our study guide data
model.fit(X_train, y_train)

print("Training Complete! The model has successfully learned the fraud patterns.")

from sklearn.metrics import classification_report, accuracy_score

# Step 1: Force the model to make predictions on the hidden exam data
predictions = model.predict(X_test)

# Step 2: Compare its guesses against the real answer key (y_test)
print("--- Final Exam Evaluation Metrics ---")
print(classification_report(y_test, predictions, target_names=['Genuine', 'Fraud']))

